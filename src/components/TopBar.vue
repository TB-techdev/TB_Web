<script setup lang="ts">
import { ref } from 'vue'
import { Icon } from '@iconify/vue'

const isMenuOpen = ref(false)

const closeMenu = () => {
  isMenuOpen.value = false
}

defineExpose({
  closeMenu,
})
</script>

<script lang="ts">
export default {
  props: ['title', 'brand'],
}

window.onscroll = function () {
  if (document.documentElement.scrollTop < 24) {
    document
      .querySelector('header')
      ?.style.setProperty(
        'padding-top',
        (32 - document.documentElement.scrollTop || 0).toString() + 'px',
      )
  } else {
    document.querySelector('header')?.style.setProperty('padding-top', '8px')
  }
}
</script>

<template>
  <header>
    <div class="brand">{{ brand }}</div>
    <Icon @click="isMenuOpen = true" icon="gg:details-more" class="iconButton" />

    <div class="menuMask" @click="closeMenu()" v-show="isMenuOpen"></div>
    <div
      class="drawer"
      :style="{
        right: isMenuOpen ? '0' : ' -10vw',
      }"
    >
      <slot></slot>
    </div>
  </header>
</template>

<style scoped>
.brand {
  height: 2rem;
